	function enlaceVerNominacion(){
		if( isV( "en_votar" ) ){
			$enl = '<i class="fa fa-hand-o-down"></i> Votar</a>';
		}
		if( isV( "en_ver_nom" ) ){
			$enl = '<i class="fa fa-eye"></i> Ver</a>';
		}
		return $enl;
	}
